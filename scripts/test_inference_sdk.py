"""Quick live test of Azure AI Inference EmbeddingsClient."""

import asyncio
import os
os.environ.setdefault("SSL_CERT_FILE", "/private/etc/ssl/cert.pem")

from dotenv import load_dotenv
load_dotenv()

async def main() -> None:
    from ai_search.clients import get_foundry_embed_client

    client = get_foundry_embed_client()

    # Test 1: text embedding
    print("Testing text embedding...")
    resp = await client.embed(
        input=["a woman in a red dress walking on a beach"],
        model="embed-v-4-0",
        dimensions=1024,
    )
    vec = resp.data[0].embedding
    print(f"  Text embedding: {len(vec)} dims, first 5: {vec[:5]}")

